const dictionaries: Record<Lang, Record<string, string>> = { en, id };

export function getStoredLanguage(): Lang {
  if (typeof window !== "undefined") {
    const stored = localStorage.getItem("cyberfrost_lang") as Lang | null;
    if (stored === "id" || stored === "en") return stored;
    const browserLang = navigator.language?.startsWith("id") ? "id" : "en";
    return browserLang;
  }
  return "en";
}

export function storeLanguage(lang: Lang): void {
  if (typeof window !== "undefined") localStorage.setItem("cyberfrost_lang", lang);
}

export function getAvailableLanguages(): { code: Lang; name: string }[] {
  return [{ code: "en", name: "English" }, { code: "id", name: "Bahasa Indonesia" }];
}

export function getTranslation(lang: Lang, key: string, fallback?: string): string {
  const dict = dictionaries[lang];
  if (dict && dict[key] !== undefined) return dict[key];
  if (fallback) return fallback;
  if (lang === "id" && en[key]) return en[key];
  return key;
}
